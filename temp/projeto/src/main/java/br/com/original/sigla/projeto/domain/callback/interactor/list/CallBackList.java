package br.com.original.sigla.projeto.domain.callback.interactor.list;

import java.time.LocalDateTime;

public class CallBackList {

    private String name;
    private String identification;
    private LocalDateTime sourceDateTime = LocalDateTime.now();
    private String classification;
    private String type;
    private String status;
    private Long value;
    private LocalDateTime creationDate;
    private LocalDateTime scheduleDate;


    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getIdentification() {
        return identification;
    }

    public void setIdentification(String identification) {
        this.identification = identification;
    }

    public LocalDateTime getSourceDateTime() {
        return sourceDateTime;
    }

    public void setSourceDateTime(LocalDateTime sourceDateTime) {
        this.sourceDateTime = sourceDateTime;
    }

    public String getClassification() {
        return classification;
    }

    public void setClassification(String classification) {
        this.classification = classification;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }

    public Long getValue() {
        return value;
    }

    public void setValue(Long value) {
        this.value = value;
    }

    public LocalDateTime getCreationDate() {
        return creationDate;
    }

    public void setCreationDate(LocalDateTime creationDate) {
        this.creationDate = creationDate;
    }

    public LocalDateTime getScheduleDate() {
        return scheduleDate;
    }

    public void setScheduleDate(LocalDateTime scheduleDate) {
        this.scheduleDate = scheduleDate;
    }
}